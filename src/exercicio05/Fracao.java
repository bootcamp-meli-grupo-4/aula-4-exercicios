package exercicio05;

public class Fracao {
    public static Double adicionar(Double numero, Double numero2){
        return numero+numero2;
    }

    public static  Integer adicionar(Integer numero, Integer numero2){
        return numero+numero2;
    }
    public static  Double subtrair(Double numero, Double numero2){
        return numero-numero2;
    }

    public static  Integer subtrair(Integer numero, Integer numero2){
        return numero-numero2;
    }
    public static  Double multiplicar(Double numero, Double numero2){
        return numero*numero2;
    }
    public static  Integer multiplicar(Integer numero, Integer numero2){
        return numero*numero2;
    }
    public static  Double dividir(Double numero, Double numero2){
        return numero/numero2;
    }
    public static  Integer dividir(Integer numero, Integer numero2){
        return numero/numero2;
    }
}
